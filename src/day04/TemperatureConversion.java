package day04;

public class TemperatureConversion {

    public static void main(String[] args) {

        double fahrenheit = 75 ;
        double celsius ;
        celsius=( 5.0/9 )* (fahrenheit-32 ) ;
        System.out.println( fahrenheit + " F equals to " + celsius + " C" );

        System.out.println("----------------------");
        double fahrenheit2 = 83;
        double celsius2;
        celsius2 = (5.0/9)* (fahrenheit2-32 ) ;
        System.out.println(fahrenheit2 +" F equals to " + celsius2 + " C");


    }
}
